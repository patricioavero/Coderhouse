# Todos los números del 0 al 10 [0, 1, 2, ..., 10]
list_of_nums = list(range(10))
print("Todos los números del 0 al 10 [0, 1, 2, ..., 10]\t->\t", list_of_nums, "\n")

# Todos los números del -10 al 0 [-10, -9, -8, ..., 0]
list_of_nums = list(range(-10, 0))
print("Todos los números del -10 al 0 [-10, -9, -8, ..., 0]\t->\t", list_of_nums, "\n")

# Todos los números pares del 0 al 20 [0, 2, 4, ..., 20]
list_of_nums = list(range(0, 21, 2))
print("Todos los números pares del 0 al 20 [0, 2, 4, ..., 20]\t->\t", list_of_nums, "\n")

# Todos los números impares entre -20 y 0 [-19, -17, -15, ..., -1]
list_of_nums = list(range(-20, 0))
print("Todos los números impares entre -20 y 0 [-19, -17, -15, ..., -1]\t->\t", list_of_nums, "\n")

# Todos los números múltiples de 5 del 0 al 50 [0, 5, 10, ..., 50]
list_of_nums = list(range(0, 51, 5))
print("Todos los números múltiples de 5 del 0 al 50 [0, 5, 10, ..., 50]\t->\t", list_of_nums)
